fun main(){

    var x : Int = 18 // variável mutável. declarando como var, eu consigo mudar essa variável depois.
    val y : Int = 12 // variável imutável. declarando como val, eu deixo a variável imutável.
    var a : Float = 1.588f // variáveis float devem terminar com f minúsculo (o número, obviamente)
    var b : Double = 22.55555555555
    var c : Long = 4343434348747876475
    var d : Char = 'A' // variáveis do tipo char são declaradas com aspas simples (o conteúdo, a letra, claro)
    var e : String = "Amanda" // já em String, o nome é com aspas duplas
    var f : Short = 12345
    var g : Byte = 0b1010 // variáveis binárias são decladas com 0b + os bits
    var h : Byte = 0xf // variáveis em hexadecimal são declaradas com 0x + o número
    var i : UInt = 12u // O U É DE SEM SINAL, ENTÃO SÓ TEM VALORES POSITIVOS. os números são terminados em u
    var j : UShort = 12345u // mesmo racioccínio para os U shorts. e assim vai com os outros tipos numéricos

    println(x)
    println(y)
    println(a)
    println(b)
    println(c)
    println(d)
    println(e)
    println(f)
    println(g)
    println(h)
    println(i)
    println(j)

    /*
    * É assim que se comenta em bloco.
    * É importante salientar, que ao declarar as variáveis os nomes dos tipos são escritos començando com letra maiús-
    * cula.
    * */

}